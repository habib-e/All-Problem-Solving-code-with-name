#include<bits/stdc++.h>
using namespace std;
class habib
{
public:
    int id;
    double gpa;
    void print()
    {
        cout<<id<<" "<<gpa<<endl;
    }

};
int main()
{
    habib h1,h2;
    h1.id=4;
    h1.gpa=5;
    h1.print();
    h2.id=8;
    h2.gpa=6;
    h2.print();


    return 0;
}
